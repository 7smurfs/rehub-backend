package sevensmurfs.rehub.enums;

public enum Role {

    /**
     * This role allows user to change admins and manage employee roles
     */
    SUPERADMIN,

    /**
     * This role allows user to control the application
     */
    ADMIN,

    /**
     * Role for patients
     */
    PATIENT,

    /**
     * Role for all employees
     */
    EMPLOYEE;

}
